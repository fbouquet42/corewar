int				ft_aatoi(char **str)
{
	int		result;
	char	sign;

	result = 0;
	sign = 1;
	if (**str == '-')
	{
		sign = -1;
		++(*str);
	}
	while (**str >= '0' && **str <= '9')
	{
		result = (result * 10) + (sign * (**str - '0'));
		++(*str);
	}
	return (result);
}
